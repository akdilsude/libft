#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*str2;
	int		i;

	str2 = malloc(sizeof(char) * ft_strlen(str) + 1);
	i = 0;
	if (!str2)
		return (NULL);
	while (str[i] != '\0')
	{
		str2[i] = str[i];
		i++;
	}
	str2[i] = '\0';
	return (str2);
}
